    public function store(Request $request,$type)
    {
        if ($type == 'question') {
            $model = Question::find($request->id);
        } else if ($type == 'answer') {
            $model = Answer::find($request->id);
        }
        $model->comments()->create([
            'user_id' => Auth::id(),
            'body' => $request->body,
        ]);
        return redirect()->route('qusetions.show',$request->question_slug);
    }

